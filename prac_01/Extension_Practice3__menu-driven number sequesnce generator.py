# Menu-driven number sequence generator:
# A school teacher requires a small program that would allow primary school
# students to learn about various number sequences.
# The teacher is interested in a simple menu-driven program
# that has the following choices (where x and y are inputs the
# user enters once at the start of the program):
# 1. Show the even numbers from x to y
# 2. Show the odd numbers from x to y
# 3. Show the squares from x to y
# 4. Exit the program